    private void CrearParqueaderoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearParqueaderoActionPerformed
 
        int CantidadPiso = Integer.parseInt(CantidadPisos.getText());
        int CantidadEspacio = Integer.parseInt(CantidadEspacioPisos.getText());
        int PrecioCarroMinuto = Integer.parseInt(PrecioCarro.getText());
        int PrecioMotoMinuto = Integer.parseInt(PrecioMoto.getText());
        
        op = new Parqueadero(CantidadPiso, CantidadEspacio, PrecioCarroMinuto,PrecioMotoMinuto);
        String Mensaje = op.estadoParqueadero();
        Ventana.setText(Mensaje);
        
   
    }//GEN-LAST:event_CrearParqueaderoActionPerformed

