    private void ParquearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ParquearActionPerformed

        int Piso = Integer.parseInt(piso.getText());
        int Espacio = Integer.parseInt(espacio.getText());
        int Tipo  = 2;
        if (Carro.isSelected()){

            Tipo = 1;

        }
        String Placa = placa.getText();

        String mensaje = op.parqueaVehiculo(Piso, Espacio, Tipo, Placa);
        JOptionPane.showMessageDialog(this, mensaje);
        String mensaje1 = op.estadoParqueadero();
        Ventana.setText(mensaje1);
    }//GEN-LAST:event_ParquearActionPerformed

    private void PagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PagoActionPerformed

        int Piso = Integer.parseInt(piso.getText());
        int Espacio = Integer.parseInt(espacio.getText());
        if(op.matris[Piso][Espacio] == null){
            JOptionPane.showMessageDialog(this, "Auto no encontrado");
            
        }else{
        Ventana.setText( op.matris[Piso][Espacio].pago());
        }

    }//GEN-LAST:event_PagoActionPerformed

